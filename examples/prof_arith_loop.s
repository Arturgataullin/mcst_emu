# длинный арифметический цикл для профилирования execute() и dispatch инструкций
# покрывает базовый горячий путь эмулятора без активной RAM

# счетчик 0x03ffffff дает сотни миллионов исполняемых инструкций эмулятора
LI R1 0xffff
LUI R1 0x03ff
LI R2 0x0001
LI R3 0x1357
LI R4 0x2468

# тело цикла начинается по байтовому адресу 0x14
ADD R0 R0 R3
XOR R0 R0 R4
MUL R5 R0 R2
SUB R1 R1 R2
BRNZ R1 0xfffc
