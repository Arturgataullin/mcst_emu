# рекурсивный factorial с ручным сохранением адреса возврата
# стек растет вниз: ASPI с отрицательным смещением выделяет место
LI R10 0x0100
SCRW SP_TOP R10
SCRW SP_SIZE R10

# R2 хранит константу 1
# R3 хранит адрес входа в функцию fact
# R0 используется как аргумент функции и как возвращаемое значение
LI R2 0x0001
LI R3 0x0024
LI R0 0x0004

# CALL записывает адрес следующей команды в R1 и переходит по адресу из R3
CALL R3

# после возврата fact(4) лежит в R0
MOV R9 R0

# переход через код функции, чтобы не выполнить его второй раз как обычный код
RJMP 0x000f

# fact(n), вход по байтовому адресу 0x24
# если 1 >= n, то дошли до базового случая fact(1)
GE R4 R2 R0
BRNZ R4 0x000b

# перед вложенным CALL нужно сохранить состояние текущего вызова
# иначе новый CALL перезапишет R1, и текущий RET потеряет адрес возврата
ASPI R5 0xfff8
STW R1 R5 0x0
STW R0 R5 0x4

# рекурсивный вызов fact(n - 1)
SUB R0 R0 R2
CALL R3

# после возврата из fact(n - 1) в R0 лежит результат рекурсивного вызова
# восстанавливаю исходный n и адрес возврата текущего вызова
LDW R6 R5 0x4
LDW R1 R5 0x0

# освобождаю кадр стека текущего вызова
ASPI R5 0x0008

# fact(n) = n * fact(n - 1)
MUL R0 R6 R0
RET

# базовый случай: fact(1) = 1
LI R0 0x0001
RET
